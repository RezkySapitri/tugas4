bilangan1 = float (input ("Masukan bilangan pertama : "))
bilangan2 = float (input("Masukan bilangan kedua : "))

if bilangan1 > bilangan2 :
    print (f"Bilangan {bilangan1} > bilangan {bilangan2}")
elif bilangan1 == bilangan2 :
    print (f"Bilangan {bilangan1} == bilangan {bilangan2}")
else :
   print (f"Bilangan {bilangan2} > bilangan {bilangan1}") 
